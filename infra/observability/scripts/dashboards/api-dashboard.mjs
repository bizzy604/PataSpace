/**
 * Purpose: Defines the "PataSpace API" Grafana dashboard — RED metrics plus
 *   Node.js runtime health for the NestJS backend.
 * Why important: The first screen an operator opens during an incident:
 *   availability, traffic, errors, latency, and process health in one view.
 * Used by: generate-dashboards.mjs (writes grafana/dashboards/pataspace-api.json).
 */
import { dashboard, stat, target, timeseries, upDownThresholds } from './helpers.mjs';

const JOB = 'job="pataspace-api"';

export const apiDashboard = dashboard({
  uid: 'pataspace-api',
  title: 'PataSpace API',
  description:
    'RED metrics and Node.js runtime health for the PataSpace API. Generated by infra/observability/scripts/generate-dashboards.mjs — do not hand-edit the JSON.',
  entries: [
    stat({
      title: 'API up',
      targets: [target(`up{${JOB}}`)],
      thresholds: upDownThresholds(),
    }),
    stat({
      title: 'Request rate',
      targets: [target(`sum(rate(http_server_requests_total{${JOB}}[5m]))`)],
      unit: 'reqps',
      decimals: 2,
    }),
    stat({
      title: '5xx error rate',
      targets: [
        target(
          `sum(rate(http_server_requests_total{${JOB},status_code=~"5.."}[5m])) / sum(rate(http_server_requests_total{${JOB}}[5m]))`,
        ),
      ],
      unit: 'percentunit',
      decimals: 2,
      thresholds: {
        mode: 'absolute',
        steps: [
          { color: 'green', value: null },
          { color: 'orange', value: 0.01 },
          { color: 'red', value: 0.05 },
        ],
      },
    }),
    stat({
      title: 'p95 latency',
      targets: [
        target(
          `histogram_quantile(0.95, sum by (le) (rate(http_server_request_duration_seconds_bucket{${JOB}}[5m])))`,
        ),
      ],
      unit: 's',
      decimals: 3,
      thresholds: {
        mode: 'absolute',
        steps: [
          { color: 'green', value: null },
          { color: 'orange', value: 0.5 },
          { color: 'red', value: 1 },
        ],
      },
    }),
    timeseries({
      title: 'Requests by route',
      targets: [
        target(`sum by (route) (rate(http_server_requests_total{${JOB}}[5m]))`, '{{route}}'),
      ],
      unit: 'reqps',
    }),
    timeseries({
      title: 'Requests by status code',
      targets: [
        target(
          `sum by (status_code) (rate(http_server_requests_total{${JOB}}[5m]))`,
          '{{status_code}}',
        ),
      ],
      unit: 'reqps',
    }),
    timeseries({
      title: 'Latency quantiles',
      targets: [
        target(
          `histogram_quantile(0.50, sum by (le) (rate(http_server_request_duration_seconds_bucket{${JOB}}[5m])))`,
          'p50',
        ),
        target(
          `histogram_quantile(0.95, sum by (le) (rate(http_server_request_duration_seconds_bucket{${JOB}}[5m])))`,
          'p95',
        ),
        target(
          `histogram_quantile(0.99, sum by (le) (rate(http_server_request_duration_seconds_bucket{${JOB}}[5m])))`,
          'p99',
        ),
      ],
      unit: 's',
    }),
    timeseries({
      title: 'In-flight requests',
      targets: [target(`http_server_requests_in_flight{${JOB}}`, 'in flight')],
    }),
    timeseries({
      title: 'Event loop lag',
      targets: [
        target(`nodejs_eventloop_lag_p50_seconds{${JOB}}`, 'p50'),
        target(`nodejs_eventloop_lag_p99_seconds{${JOB}}`, 'p99'),
      ],
      unit: 's',
      w: 8,
    }),
    timeseries({
      title: 'Memory',
      targets: [
        target(`process_resident_memory_bytes{${JOB}}`, 'RSS'),
        target(`nodejs_heap_size_used_bytes{${JOB}}`, 'heap used'),
        target(`nodejs_heap_size_total_bytes{${JOB}}`, 'heap total'),
      ],
      unit: 'bytes',
      w: 8,
    }),
    timeseries({
      title: 'Process CPU',
      targets: [target(`rate(process_cpu_seconds_total{${JOB}}[5m])`, 'CPU')],
      unit: 'percentunit',
      w: 8,
    }),
  ],
});
