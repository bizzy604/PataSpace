/**
 * Purpose: Defines the "PataSpace Infrastructure" Grafana dashboard — host,
 *   container, PostgreSQL, and Redis health.
 * Why important: When the API dashboard looks bad, this one says why: CPU,
 *   memory, disk, database connections, or cache pressure.
 * Used by: generate-dashboards.mjs (writes grafana/dashboards/pataspace-infrastructure.json).
 */
import { dashboard, stat, target, timeseries, upDownThresholds } from './helpers.mjs';

export const infraDashboard = dashboard({
  uid: 'pataspace-infrastructure',
  title: 'PataSpace Infrastructure',
  description:
    'Host, container, PostgreSQL, and Redis health for the PataSpace stack. Generated by infra/observability/scripts/generate-dashboards.mjs — do not hand-edit the JSON.',
  entries: [
    stat({
      title: 'PostgreSQL up',
      targets: [target('pg_up')],
      thresholds: upDownThresholds(),
    }),
    stat({
      title: 'Redis up',
      targets: [target('redis_up')],
      thresholds: upDownThresholds(),
    }),
    stat({
      title: 'Targets up',
      targets: [target('count(up == 1)')],
      decimals: 0,
    }),
    stat({
      title: 'Targets down',
      targets: [target('count(up == 0) or vector(0)')],
      decimals: 0,
      thresholds: {
        mode: 'absolute',
        steps: [
          { color: 'green', value: null },
          { color: 'red', value: 1 },
        ],
      },
    }),
    timeseries({
      title: 'Host CPU usage',
      targets: [
        target('100 * (1 - avg(rate(node_cpu_seconds_total{mode="idle"}[5m])))', 'used %'),
      ],
      unit: 'percent',
      max: 100,
    }),
    timeseries({
      title: 'Host memory',
      targets: [
        target(
          'node_memory_MemTotal_bytes - node_memory_MemAvailable_bytes',
          'used',
        ),
        target('node_memory_MemTotal_bytes', 'total'),
      ],
      unit: 'bytes',
    }),
    timeseries({
      title: 'Disk usage by mountpoint',
      targets: [
        target(
          '100 * (1 - node_filesystem_avail_bytes{fstype!~"tmpfs|overlay|squashfs"} / node_filesystem_size_bytes)',
          '{{mountpoint}}',
        ),
      ],
      unit: 'percent',
      max: 100,
    }),
    timeseries({
      title: 'Host network',
      targets: [
        target('sum(rate(node_network_receive_bytes_total{device!~"lo|veth.*|br.*|docker.*"}[5m]))', 'rx'),
        target('sum(rate(node_network_transmit_bytes_total{device!~"lo|veth.*|br.*|docker.*"}[5m]))', 'tx'),
      ],
      unit: 'Bps',
    }),
    timeseries({
      title: 'Container CPU',
      targets: [
        target('sum by (name) (rate(container_cpu_usage_seconds_total{name!=""}[5m]))', '{{name}}'),
      ],
      unit: 'percentunit',
    }),
    timeseries({
      title: 'Container memory (working set)',
      targets: [
        target('container_memory_working_set_bytes{name!=""}', '{{name}}'),
      ],
      unit: 'bytes',
    }),
    timeseries({
      title: 'PostgreSQL connections',
      targets: [
        target('sum(pg_stat_activity_count)', 'active'),
        target('min(pg_settings_max_connections)', 'max_connections'),
      ],
    }),
    timeseries({
      title: 'PostgreSQL transactions',
      targets: [
        target('sum(rate(pg_stat_database_xact_commit{datname!=""}[5m]))', 'commits'),
        target('sum(rate(pg_stat_database_xact_rollback{datname!=""}[5m]))', 'rollbacks'),
      ],
      unit: 'ops',
    }),
    timeseries({
      title: 'Redis memory',
      targets: [
        target('redis_memory_used_bytes', 'used'),
        target('redis_memory_max_bytes != 0', 'maxmemory'),
      ],
      unit: 'bytes',
    }),
    timeseries({
      title: 'Redis activity',
      targets: [
        target('rate(redis_commands_processed_total[5m])', 'commands/s'),
        target('redis_connected_clients', 'clients'),
      ],
    }),
    timeseries({
      title: 'Redis keyspace hit ratio',
      targets: [
        target(
          'rate(redis_keyspace_hits_total[5m]) / (rate(redis_keyspace_hits_total[5m]) + rate(redis_keyspace_misses_total[5m]))',
          'hit ratio',
        ),
      ],
      unit: 'percentunit',
      max: 1,
      w: 24,
    }),
  ],
});
